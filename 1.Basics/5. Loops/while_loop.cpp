// while loop in C++
#include <iostream>
using namespace std;

int main(){

    //initialization expression
    int i = 1;

    //test expression
    while(i < 11){

        //Printing table of 2
        cout << "2 x " << i << " = " << i*2 << endl;

        //update expression
        i++;
    }
    return 0;
}
